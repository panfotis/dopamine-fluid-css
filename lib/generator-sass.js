'use strict';

/**
 * Generate the _dopamine-functions.scss content from config.
 */
function generateSassFunctions(config) {
  const vpMin = config.viewport.min;
  const vpMax = config.viewport.max;

  const bpEntries = Object.entries(config.breakpoints)
    .sort((a, b) => a[1] - b[1]);

  const bpMap = bpEntries
    .map(([name, px]) => `  "${name}": ${px}px`)
    .join(',\n');

  return [
    `// ================================================`,
    `// Generated by Dopamine Fluid`,
    `// Default viewport: ${vpMin}px – ${vpMax}px`,
    `// ================================================`,
    ``,
    `@use "sass:math";`,
    `@use "sass:map";`,
    ``,
    `/// Fluid clamp value that scales linearly between two viewport widths.`,
    `///`,
    `/// @param {Number} $min - Minimum value in pixels`,
    `/// @param {Number} $max - Maximum value in pixels`,
    `/// @param {Number} $vpMin [${vpMin}] - Minimum viewport width in pixels`,
    `/// @param {Number} $vpMax [${vpMax}] - Maximum viewport width in pixels`,
    `/// @return {String} - CSS clamp() value`,
    `///`,
    `/// @example scss`,
    `///   font-size: fluid(16, 48);`,
    `///   padding: fluid(12, 32);`,
    `///   font-size: fluid(32, 96, 480, 1920); // custom viewport`,
    ``,
    `@function fluid($min, $max, $vpMin: ${vpMin}, $vpMax: ${vpMax}) {`,
    `  $minRem: math.div($min, 16) * 1rem;`,
    `  $maxRem: math.div($max, 16) * 1rem;`,
    `  $slope: math.div(($max - $min), ($vpMax - $vpMin));`,
    `  $intercept: math.div($min - $slope * $vpMin, 16);`,
    `  $vw: $slope * 100;`,
    ``,
    `  @if $intercept == 0 {`,
    `    @return clamp(#{$minRem}, #{$vw}vw, #{$maxRem});`,
    `  } @else if $intercept > 0 {`,
    `    @return clamp(#{$minRem}, calc(#{$vw}vw + #{$intercept * 1rem}), #{$maxRem});`,
    `  } @else {`,
    `    @return clamp(#{$minRem}, calc(#{$vw}vw - #{$intercept * -1rem}), #{$maxRem});`,
    `  }`,
    `}`,
    ``,
    `// Breakpoints`,
    `$breakpoints: (`,
    `${bpMap}`,
    `);`,
    ``,
    `/// Apply styles at a minimum viewport width (mobile-first).`,
    `/// @param {String} $name - Breakpoint name (${bpEntries.map(([n]) => n).join(', ')})`,
    `@mixin breakpoint-up($name) {`,
    `  @media (min-width: map.get($breakpoints, $name)) {`,
    `    @content;`,
    `  }`,
    `}`,
    ``,
    `/// Apply styles below a viewport width.`,
    `/// @param {String} $name - Breakpoint name (${bpEntries.map(([n]) => n).join(', ')})`,
    `@mixin breakpoint-down($name) {`,
    `  @media (max-width: map.get($breakpoints, $name) - 0.02px) {`,
    `    @content;`,
    `  }`,
    `}`,
    ``,
  ].join('\n');
}

module.exports = { generateSassFunctions };
